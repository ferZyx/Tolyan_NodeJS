import {bot, userLastRequest} from "../../app.js";
import log from "../../logging/logging.js";

export async function commandAntiSpamMiddleware(msg, next) {
    try {
        const userId = msg.chat.id;
        const currentTime = new Date().getTime();

        // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –±—ã–ª –ª–∏ –ø—Ä–µ–¥—ã–¥—É—â–∏–π –∑–∞–ø—Ä–æ—Å –æ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        if (userLastRequest[userId]) {
            const timeDiff = currentTime - userLastRequest[userId];

            // –ï—Å–ª–∏ –ø—Ä–æ—à–ª–æ –º–µ–Ω–µ–µ —Å–µ–∫—É–Ω–¥—ã —Å –ø—Ä–µ–¥—ã–¥—É—â–µ–≥–æ –∑–∞–ø—Ä–æ—Å–∞, —Å—á–∏—Ç–∞–µ–º —ç—Ç–æ —Å–ø–∞–º–æ–º
            if (timeDiff < 1000) {
                // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ –æ —Å–ø–∞–º–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é
                await bot.sendMessage(msg.chat.id, "üöØ –ù–µ —Å–ø–∞–º—å! ", {reply_to_message_id: msg.message_id})
                    .catch(e => {
                        log.error(`User ${msg.chat.id} got an error –≤ command –∞–Ω—Ç–∏—Å–ø–∞–º –º–∏–¥–ª–≤–∞—Ä–µ` + e.message, {stack: e.stack})
                    })
                // –ü—Ä–æ–ø—É—Å–∫–∞–µ–º –≤—ã–ø–æ–ª–Ω–µ–Ω–∏–µ —Ö–µ–Ω–¥–ª–µ—Ä–∞
                return;
            }
        }

        // –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤—Ä–µ–º—è —Ç–µ–∫—É—â–µ–≥–æ –∑–∞–ø—Ä–æ—Å–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        userLastRequest[userId] = currentTime;

        // –í—ã–ø–æ–ª–Ω—è–µ–º —Å–ª–µ–¥—É—é—â–∏–π —Ö–µ–Ω–¥–ª–µ—Ä
        await next();
    } catch (e) {
        log.error("–í–ê–ñ–ù–û! –û—à–∏–±–∫–∞ –≤ commandAntiSpamMiddleware. " + e.message, {stack: e.stack})
    }

}